      <h2>Welcome to AOJD-Online.net<br>
      </h2>
      <p>Building on he work of Rabbi Stern, <b>AOJD-online.net</b> travels well beyond the original concept of Rabbi Stern's books.  Stern originally focused on Jewish families in the U. S. and Canada prior to 1830. He most often worked from direct family correspondence, synagogue records, early cemetery records and only occasionally from government published data and other public records. </p>
      
      <p>The new AOJD database makes Rabbi Stern's work both more accessible (full search capabilities on all data fields) . The new database also reflects the 50+ pages of corrections and additions suggested by Rabbi Stern in the 2<sup>nd</sup> and 3<sup>rd</sup> editions.  New corrections and missed families and individuals then extend the available information.</p><p>Lastly, the new information set goes further by beginning the task of tracking numerous families that arrived between 1830 and 1870. In particular the links between major mercantile and political families become evident as the population of the U. S. grows and we approach the era of mass Eastern European immigration.</p>

<p>The AOJD-online database is being carefully researched to current genealogical standards to prove much of the family information published in the earlier work.  New sources include U.S. and local government records from vital records (birth, marriage, death, divorce, probate, deeds, etc.) to census, ship arrivals, immigration and naturalization, and military records as well as local historical newspapers. In additional to drawing from commercial online databases, on-site research has been conducted from Montreal, Canada throughout the U. S. and into the Caribbean Islands where many of these families originated.  Small steps in bridging the genealogical gap between the colonial U. S. and both Great Britain and the Netherlands have already begun to enter into this data collection.</p>


<p>As Rabbi Stern discovered, many of the families whose lineage is represented herein are enthusiastic supporters of this project.  They continue to share updates and modifications (documentation is required) most generously and several new lines have been added.  In particular, we want to acknowledge the sharing and extend our great thanks to Anne Josephs (Montreal, Canada), Jonathan de Sola-Mendes (New York City), Ainsley Henriques (Kingston, Jamaica), Louis Kessler (Mikve Israel Archives, Philadelphia), and many others.</p>

Proofing, corrections, and new data entry are an ongoing effort of Kate Kleiman and the staff at Heritage Muse, Inc.  The on-line database is updated quarterly. </div>
